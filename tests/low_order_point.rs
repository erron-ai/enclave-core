#[test]
fn low_order_points_are_rejected() {
    let secret = StaticSecret::from([1u8; 32]);
    let mut rejected_any = false;
    for bytes in LOW_ORDER_POINTS.iter() {
        let peer = PublicKey::from(*bytes);
        match x25519_shared_secret_checked(&secret, &peer) {
            Err(EcdhError::DegeneratePoint) => rejected_any = true,
            Ok(_) => {}
            Err(_) => {}
        }
    }
    assert!(
        rejected_any,
        "expected at least one low-order point to produce a degenerate shared secret"
    );
}
